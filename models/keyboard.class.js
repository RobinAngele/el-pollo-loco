/**
 * Keyboard class for tracking keyboard input states
 */
class Keyboard {
    /**
     * State of the left arrow key
     * @type {boolean}
     */
    LEFT = false;
    
    /**
     * State of the right arrow key
     * @type {boolean}
     */
    RIGHT = false;
    
    /**
     * State of the up arrow key
     * @type {boolean}
     */
    UP = false;
    
    /**
     * State of the down arrow key
     * @type {boolean}
     */
    DOWN = false;
    
    /**
     * State of the space key
     * @type {boolean}
     */
    SPACE = false;
    
    /**
     * State of the T key (used for throwing bottles)
     * @type {boolean}
     */
    T = false;
    
    /**
     * State of the ESC key
     * @type {boolean}
     */
    ESC = false;
    
    /**
     * State of the B key (used for exchanging coins for bottles)
     * @type {boolean}
     */
    B = false;
}